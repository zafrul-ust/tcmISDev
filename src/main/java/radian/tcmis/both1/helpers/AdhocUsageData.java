    public static Vector getVector(Vector in,int size)
    {
        Vector out = new Vector();
        try
        {
          for(int i = 0; i < in.size(); i++)
          {
              Hashtable h = (Hashtable)in.elementAt(i);
              out.addElement(new AdhocUsageData(h, h.size()));
          }
        }
        catch(Exception e11)
        {
            e11.printStackTrace();
        }
        return out;
    }
